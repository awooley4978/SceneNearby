// ── Actor Groups ──
export function buildActorGroups(): ActorGroup[] {
  const groups = new Map<string, ActorGroup>();
  for (const loc of allLocationsWithActors) {
    for (const actor of loc.actors || []) {
      if (!groups.has(actor)) {
        groups.set(actor, { name: actor, locationIds: [], showTitles: [] });
      }
      const g = groups.get(actor)!;
      if (!g.locationIds.includes(loc.id)) {
        g.locationIds.push(loc.id);
      }
      if (!g.showTitles.includes(loc.movieOrShow)) {
        g.showTitles.push(loc.movieOrShow);
      }
    }
  }
  return Array.from(groups.values()).sort((a, b) => a.name.localeCompare(b.name));
}

