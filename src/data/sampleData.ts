// ── Movie Groups ──
function buildMovieGroups(): MovieGroup[] {
  const groups = new Map<string, MovieGroup>();
  for (const loc of allLocations) {
    const key = `${loc.movieOrShow}||${loc.year}`;
    if (!groups.has(key)) {
      groups.set(key, {
        title: loc.movieOrShow,
        year: loc.year,
        isMovie: loc.isMovie,
        category: loc.category,
        locationIds: [],
        locationCount: 0,
      });
    }
    const g = groups.get(key)!;
    g.locationIds.push(loc.id);
    g.locationCount = g.locationIds.length;
  }
  return Array.from(groups.values()).sort((a, b) => a.title.localeCompare(b.title));
}

export const movieGroups = buildMovieGroups();

export const locationsByMovie = (movieTitle: string): FilmingLocation[] =>
  allLocations.filter((l) => l.movieOrShow === movieTitle);

export const movieGroupByTitle = (title: string): MovieGroup | undefined =>
  movieGroups.find((g) => g.title === title);
